package model;

public class Cargo {
    int id;
    int weight;
    String weightUnit;
    int pieces;

    public int getId() {
        return id;
    }

    public int getWeight() {
        return weight;
    }

    public String getWeightUnit() {
        return weightUnit;
    }

    public int getPieces() {
        return pieces;
    }
}
